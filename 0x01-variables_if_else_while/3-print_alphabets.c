#include <stdio.h>

/**
 * main - prints the alphabet in lowercase, uppercase then new line
 * Return: Always 0 (Success)
 */

int main(void)
{
	char c;

	char d;

	c = 'a';
	d = 'A';
	while
		(c <= 'z') {
			putchar(c);
			c++;
		}
	while
		(d <= 'z') {
			putchar (d);
			d++;
		}
	putchar('\n');
	return (0);
}
